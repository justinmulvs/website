<h1>
	Predictably Irrational
</h1>

<p>Turns out we humans aren't always perfectly rational, and you can actually predict in what ways we aren't. Hence the title *snicker*.</p><br>

</div>

<div class="notes">

	<div><u>1. The Truth About Relativity</u></div>
	<div><u><br/></u></div>
	<ol>
	<li>We don’t know what we want until we see it in context. <b>We compute relatively.<br/></b></li>
	<li><b>We tend to compare things which are easy to compare.</b> (<i>ex.</i> Two colonial houses rather than a colonial and a contemporary. This is harder).<br/></li>
	<li><b>Relativity:</b> If we have objects A, B, -A, where A and B are not well compared but A is slightly better than -A, we will generally choose A of all three. It<i> seems</i> best.</li>
	<li><b>Introducing a related option can make it easier to choose</b>, if done in a way that creates a sense of better and worse. <i>Ex: </i>If someone is a wingman have them be slightly less attractive, you seem better and are more likely to be chosen</li>
	<li><b>It’s hard to compare across categories</b>. Think career fields. It's hard to compare which we prefer, introduces decision fatigue. The question “do I want a new microwave or a new oven?” vs. “Do I want this oven or that one?"</li>
	<li>The more we have the more we want— hedonistic adaptation. <b>To break the cycle, take a broad view. Think in terms of where $$ can be spent and overall utility.</b></li>
	<li><b><span style="color: rgb(255, 38, 0);">Practical Implications: </span></b>
	<ol>
	<li><span style="color: rgb(255, 38, 0);">Introduce a slightly less attractive option to sell something, including yourself.</span></li>
	<li><span style="color: rgb(255, 38, 0);">Break cycles by thinking in terms of absolute $$ and time, not solely by comparing to another available option.<br/>
	<br/></span></li>
	</ol>
	</li>
	</ol>
	<div><u>2. The Fallacy of Supply and Demand</u></div>
	<ol>
	<li><b>Imprinting/Anchoring:</b> Can imprint to prices, quality standards, or demographics. <b>Where a product first shows up is relevant.</b> What is it associated with&gt;</li>
	<li><b>Arbitrary Coherence:</b> initial determinations (in price, for example) can be arbitrary, but will pave the way for and determine the future. The laws of supply and demand run relative to the initial price, they are not absolutely correct.</li>
	<li><b>An anchor for a product</b> (say a bottle of wine) determines what we find reasonable for another in that category (a nicer bottle of wine)</li>
	<li><b>We stay committed to an initial anchor</b> even after new ones have been introduced— <i>so long as we remember them.</i></li>
	<li><b>Anchors persist over time</b><i> </i>(depending on if you start with nice beer or mediocre, one seems expensive or cheap. Same for food)</li>
	<li><b>We anchor to past decisions</b>, to the point where we no longer ask why we do things and if they’re worth it. We just do them.</li>
	<li>If you differentiate enough, previous anchors are irrelevant (think Starbucks and Dunkin’ Donuts- they’re in a different category).</li>
	<li><b>Asking for money vs. Offering payment</b> for the same thing can lead consumers to call something pleasurable or burdensome. There’s actual research on it.</li>
	<li><b>Willingness to pay is easily manipulated.</b> It is based on anchors and our memory, not strictly preference.</li>
	<li><b><span style="color: rgb(255, 38, 0);">Practical Implications:</span></b>
	<ol>
	<li><span style="color: rgb(255, 38, 0);">Question your habits, their origins, and if the time and money you’re investing would be better put elsewhere.</span></li>
	<li><span style="color: rgb(255, 38, 0);">Pay attention to your first decisions regarding food, clothing, housing, etc. They can determine all of your impressions going forward.</span></li>
	<li><span style="color: rgb(255, 38, 0);">Use utility and pleasure as indicators of exchanges, not price relative to before.<br/>
	<br/></span></li>
	</ol>
	</li>
	</ol>
	<div><u>3. The Cost of Zero Cost</u></div>
	<ol>
	<li>There is a <b>fallacy</b> where we will take things that we’ll eventually need to throw away, or invest irrational amounts of time because they're <i>free</i>.</li>
	<li><b>There is a disproportionate shift in demand from the shift from $2 to $1 and $1 to $0.</b> Use freebies in business rather than discounts to drive more business.</li>
	<li><b>Free is an issue when it distracts us from what we really want.</b> (We get crappy socks we don’t want rather than high quality. We eat until we feel disgusting rather than eating less and having it be better.)</li>
	<li><b>Free removes fear of loss</b>-  Leads to irrational decisions, since fear of loss is a tremendous motivator for humans.</li>
	<li><b>Free generally leads us to jump and not analyze</b> (Trade one small chocolate for a large, or just get another small for free! That large may net you more, but you don’t compute because it’s free.)</li>
	<li><span style="color: rgb(255, 38, 0);"><b>Practical Implications:</b></span>
	<ol>
	<li><span style="color: rgb(255, 38, 0);">Look at everything you do because it is free. Do a <u>real</u> computation. Is it worth it? Is there better use of your time and money?<br/>
	<br/></span></li>
	</ol>
	</li>
	</ol>
	<div><u>4. The cost of social norms:</u></div>
	<ol>
	<li><b>Social norms:</b>  Generosity, reciprocity</li>
	<li><b>Market Norms:</b> Money, Value</li>
	<li>Issues arises when the two collide (spending money on dates and desiring sex).</li>
	<li><b>Under market norms, money increases effort. Under purely social norms, people naturally work hard.</b> For lawyers, pro-bono will be more motivating than receiving little payment.</li>
	<li><b>Gifts work well to maintain social norms.</b> But don’t mention money. As soon as money is even psychologically primed, Market behaviors arise.</li>
	<li>Replacing Social Norms with market brings about Market behaviors.</li>
	<li><b>Business Implications:</b> Building social relationships with customers must be all-encompassing. Customer service must treat them like family as well.</li>
	<li><b>Business 2:</b> If you build social relationships with employees, it must be two ways. <b>Gifts will mean more than equal amounts of money</b>. Free lunches, alcohol, surprises.</li>
	<li><b>Burning Man:</b> Gift economy, bred kindness and compassion (although lacked some practicality)</li>
	<li><span style="color: rgb(255, 38, 0);"><b>Practical Implications:</b></span>
	<ol>
	<li><span style="color: rgb(255, 38, 0);">Thinking about money reduces cooperation and kindness, increases selfish behaviors.</span></li>
	<li><span style="color: rgb(255, 38, 0);">Keep Money away from social relationships/engagements.</span></li>
	<li><span style="color: rgb(255, 38, 0);">Involve money for things directly considered to be work.</span></li>
	<li><span style="color: rgb(255, 38, 0);">Social reciprocity may be more valuable than Market norms/money.<br/>
	<br/></span></li>
	</ol>
	</li>
	</ol>
	<div><u>5. The Influence of Arousal</u></div>
	<ol>
	<li>Sexual arousal increases the likelihood of unsafe and immoral activities even more than we expect.</li>
	<li>We must protect ourselves and move slow when impassioned, we need to prepare for it. <u>Bring a condom, fool.</u><br/>
	<br/></li>
	</ol>
	<div><u>6. The Problem of Procrastination and Self-Control</u></div>
	<ol>
	<li>Emotions explain why we cheat on diets, don’t save money, make bad decisions. We can’t control them in the moment.</li>
	<li><b>Rigid deadlines are best for procrastination, tools for permitting help</b></li>
	<li><b>Automate savings, schedule exercise, only buy healthy food.</b> Pre-commit.</li>
	<li>Put money somewhere and only let it be refunded if you follow through with the behavior you’re trying to create. Donate it to charity otherwise.<br/>
	<br/></li>
	</ol>
	<div><u>7. The High Price of Ownership</u></div>
	<ol>
	<li><b>We overvalue things we own or expect to</b> (like in an auction), due to fear of potential loss rather than gain.</li>
	<li>Disagreements arise from this when owners overvalue relative to others and expect others to see their perspective.</li>
	<li><b>More work to get something=Greater Sense of ownership=More irrationality</b></li>
	<li>Marketing/advertising succeeds when it makes people imagine themselves as owners.</li>
	<li><b>Ownership applies to ideas as well.</b> Try to view as a non-owner, take ideas for trial periods. When we feel we own an idea, we will struggle to abandon it, even if it’s bad<br/>
	<br/></li>
	</ol>
	<div><u>8. Keeping Doors Open</u></div>
	<ol>
	<li><b>Fear of loss motivates us to keep even bad options open</b>. Even if we know we can re-open them.</li>
	<li>We often don’t realize these doors in our day-to-day, merely in work. But when deciding we can miss out on family, friends, etc.</li>
	<li><span style="color: rgb(255, 38, 0);"><b>Consider the cost of not letting options go</b>. Let doors both small and large close.<br/>
	<br/></span></li>
	</ol>
	<div><u>9. The Effects of Expectations</u></div>
	<ol>
	<li><b>Presentation/Information alters expectations which alters actual experience and preference.</b></li>
	<li>Providing strange information AFTER having someone try something has no negative effects, before does.</li>
	<li>Adding exotic flavors/nice presentation heightens expectations and experience (Think wine in solo cups vs. nice glasses).</li>
	<li><b>Marketing uses this to heighten anticipated and real pleasure</b>.</li>
	<li>Stereotypes are an example of expectations altering.</li>
	<li><b>Stereotypes change our experience of others, and how we perform.</b> Via priming.</li>
	<li><span style="color: rgb(255, 38, 0);"><b>Use blind conditions:</b> Have ideas presented independent of individuals.<br/>
	<br/></span></li>
	</ol>
	<div><u>10. The Power of Price</u></div>
	<ol>
	<li><b>Placebos are caused by:</b> Belief: We have confidence in a factor (price, individual, system) and Conditioning: Our body is conditioned to react a certain way due to previous experience.</li>
	<li>Paying higher prices makes us <i>feel</i> better, especially if we had recent experiences with a product/problem it solves (buy better painkillers if in pain).</li>
	<li><b>Conscious acknowledgement</b> <b>of a placebo can alleviate it.</b> But you have to believe it.</li>
	<li>Placebos can ignite hormonal changes (seeing a painkiller causes our brain to release relief hormones).<br/>
	<br/></li>
	</ol>
	<div><u>11+12. The Context of Our Character:</u></div>
	<ol>
	<li><b>Two Types of Dishonesty:</b> “Crooks” and “White Lies”. They can be equally as damaging over time, but we don’t feel or interpret it that way</li>
	<li><b>If capable, most people will cheat a little</b>. Almost everyone cheats a little bit, but it doesn’t get worse as they have higher chances of getting away with it.</li>
	<li>Cost-Benefit Analyses drive people to be honest and dishonest, social harmony is the driver.</li>
	<li><b>Small Lies don’t activate our moral compass/superego.</b> &lt;— Part of our consciousness which desires to adhere to social norms, it will lie freely if this isn’t violated.</li>
	<li><span style="color: rgb(255, 38, 0);"><b>Priming morality (even secular codes) encourages honest behavior.</b> Although it must be done in the moment or ver close to a moral decision.</span></li>
	<li><b>Immorality is easier when distance is added. easier to steal when not directly money.</b> It’s also easier when YOU are not directly to gain (get it for someone else, etc…)<br/>
	<br/></li>
	</ol>
	<div><u>13. Beer and Free Lunches</u></div>
	<ol>
	<li>Our desire to be unique can lower utility in public situations. Decide and commit ahead of time to offset (think eating in a restaurant, you’re decision will change based on others decisions).</li>
	<li><span style="color: rgb(255, 38, 0);"><b>Free Lunches:</b> Take advantage of our irrationalities and fix them so that everyone wins. ex: Company has employees opt-in to commit a percentage of future raises to savings. More Savings, Happier Employees.</span></li>
	</ol>
